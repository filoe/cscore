namespace CSCore.SoundOut.AL
{
    internal enum ALFormat
    {
        /// <summary>
        /// Unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Mono, 8Bit.
        /// </summary>
        Mono8Bit = 0x1100,

        /// <summary>
        /// Mono, 16Bit.
        /// </summary>
        Mono16Bit = 0x1101,

        /// <summary>
        /// Stereo, 8Bit.
        /// </summary>
        Stereo8Bit = 0x1102,

        /// <summary>
        /// Stereo, 16Bit.
        /// </summary>
        Stereo16Bit = 0x1103
    }
}
